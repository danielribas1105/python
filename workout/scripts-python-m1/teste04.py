a=input('Digite algo?')
print('Valor digitado',a)
print('O valor digitado é número?',a.isnumeric())
print('O valor digitado é string?',a.isalpha())
print('O valor digitado é alfanúmérico?',a.isalnum())
print('O valor digitado está em maiúculas?',a.isupper())
print('O valor digitado está em minúsculas?',a.islower())
print('O valor digitado está capitalizado?',a.istitle())

